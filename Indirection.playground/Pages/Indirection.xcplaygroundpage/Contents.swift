Indirection
==========
 
 **1.** The fundamental theorem of software engineering (FTSE) is a term originated by Andrew Koenig to describe a remark by Butler Lampson attributed to the late David J. Wheeler:
 >We can solve any problem by introducing an extra level of indirection.
 >
 >**Source:** [wikipedia.org](https://en.wikipedia.org/wiki/Fundamental_theorem_of_software_engineering)

 
 **2.** Higher-level examples of indirection are the design patterns of the proxy and the proxy server
 >Delegation is another classic example of an indirection pattern
 >
 >**Source:** [wikipedia.org](https://en.wikipedia.org/wiki/Indirection)
 
 
 **3.** Where to assign a responsibility to avoid direct coupling between two or more things?
 >Assign the responsibility to an intermediate object to mediate between other components or services so that they are not directly coupled.
 >
 >**Source:** [GRASP](http://www.kamilgrzybek.com/design/grasp-explained/)
 
